List<String> getAllArtists() {
  return artistSongs.keys.toList();
}

List<String> getSongsByArtist(String artist) {
  return artistSongs[artist]?['songs']
          ?.map((song) => song['title'] ?? '')
          .toList() ??
      [];
}

String getGenreBySong(String artist, String song) {
  return artistSongs[artist]?['songs']
          ?.firstWhere((element) => element['title'] == song)['genre'] ??
      '';
}

String getAudioBySong(String artist, String song) {
  return artistSongs[artist]?['songs']
          ?.firstWhere((element) => element['title'] == song)['audio'] ??
      '';
}
